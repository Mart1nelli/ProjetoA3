    private void noticiasComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_noticiasComboBoxActionPerformed
        // TODO add your handling code here:
        try{
            NoticiaDAO dao = new NoticiaDAO();
            Noticia [] noticias = dao.obterNoticias();
            int posicao = noticiasComboBox.getSelectedIndex();
            String titulo = noticias[posicao].getTitulo();
            String resumo = noticias[posicao].getResumo();
            String url = noticias[posicao].getUrl();
            
            txf_titulo.setText(titulo);
            txf_resumo.setText(resumo);
            txf_url.setText(url);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Tente mais tarde");
            e.printStackTrace();
        }
    }//GEN-LAST:event_noticiasComboBoxActionPerformed

    private void btn_novoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_novoActionPerformed
        // TODO add your handling code here:
        try{
            String titulo = txf_titulo.getText();
            String resumo = txf_resumo.getText();
            String url = txf_url.getText();
            Noticia noticia = new Noticia(titulo, resumo, url);
            NoticiaDAO dao = new NoticiaDAO();
            dao.criaNoticia(noticia);
            JOptionPane.showMessageDialog(null, "Noticia criada com sucesso!");
            txf_titulo.setText("");
            txf_resumo.setText("");
            txf_url.setText("");
            buscarNoticias();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Problemas técnicos");
            e.printStackTrace();
        }    
    }//GEN-LAST:event_btn_novoActionPerformed

    private void btn_atualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_atualizarActionPerformed
        // TODO add your handling code here:
        try {
            NoticiaDAO dao = new NoticiaDAO();
            Noticia [] noticias = dao.obterNoticias();
            int posicao = noticiasComboBox.getSelectedIndex();
            String titulo = txf_titulo.getText();
            String resumo = txf_resumo.getText();
            String url = txf_url.getText();
            noticias[posicao].setTitulo(titulo);
            noticias[posicao].setResumo(resumo);
            noticias[posicao].setUrl(url);
            dao.atualizaNoticia(noticias[posicao]);
            JOptionPane.showMessageDialog(null, "Noticia atualizada com sucesso");
            buscarNoticias();
           
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Problemas técnicos volte mais tarde");
            e.printStackTrace();
        }
    }//GEN-LAST:event_btn_atualizarActionPerformed

    private void btn_removerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_removerActionPerformed
        // TODO add your handling code here:
        try {
            NoticiaDAO dao = new NoticiaDAO();
            Noticia [] noticias = dao.obterNoticias();
            int posicao = noticiasComboBox.getSelectedIndex();
            dao.deletaNoticia(noticias[posicao]);
            JOptionPane.showMessageDialog(null, "Noticia excluida com sucesso");
            txf_titulo.setText("");
            txf_resumo.setText("");
            txf_url.setText("");
            buscarNoticias();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Problemas técnicos tente novamente mais tarde");
            e.printStackTrace();
        }
    }//GEN-LAST:event_btn_removerActionPerformed

